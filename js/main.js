var gary = {
  name: "Gary",
  pokemon: [pokemonA, pokemonB, pokemonC, pokemonD, pokemonE, pokemonF]
}

var rotatePokeball = function(){
  this.classList.toggle('rotate');
}

var game = {
  player1: ash,
  player2: gary,
  start: function(){
    // Name
    p1FirstPokemon = this.player1.pokemon[0];
    p2FirstPokemon = this.player2.pokemon[0];

    document.querySelector('#player-1 .name').innerText = p1FirstPokemon.name;
    document.querySelector('#player-2 .name').innerText = p2FirstPokemon.name;

    //Avatar
    document.querySelector('#player-1 .avatar img').setAttribute('src', p1FirstPokemon.avatar.back);
    // document.querySelector('#player-2 .avatar img').setAttribute('src', p2FirstPokemon.avatar.front);

    //Moves
    for(var i = 0; i < p1FirstPokemon.moves.length; i++){
      document.querySelector('#player-1 #move-' + (i+1)).innerText = p1FirstPokemon.moves[i].name;
      document.querySelector('#player-1 #move-' + (i+1)).setAttribute('class', p1FirstPokemon.moves[i].element);
    }

    //Set party pokemon
    var partyPokemon = document.querySelectorAll('#player-1 .party');
    for(var i = 0; i < partyPokemon.length; i++){
      // Set listener to rotate and un-rotate
      partyPokemon[i].querySelector('img').addEventListener('mouseenter', rotatePokeball);
      partyPokemon[i].querySelector('img').addEventListener('mouseout', rotatePokeball);
    }
  }
};

game.start();


